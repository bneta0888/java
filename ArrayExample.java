public class ArrayExample {

    public static void main(String[] args) {
        
        //Declare an array of integers
        int[] myArray;

        //Instantiate the array
    myArray = new int [5];

    //Initialize the array
    myArray[0] = 10;
    myArray[1] = 20;
    myArray[2] = 30;
    myArray[3] = 40;
    myArray[4] = 50;


    // Print the array
    for(int i = 0; i < myArray.length; i++){
        System.out.println(myArray[i]);
    }




    }
    
}
